const fs = require('node:fs');
const path = require('node:path');

const ROOT = path.join(__dirname, '..');
const sourcePath = path.join(ROOT, 'data', 'tpg-programmes.json');
const targetPath = path.join(ROOT, 'miniprogram', 'utils', 'tpgCatalog.js');

const catalogue = JSON.parse(fs.readFileSync(sourcePath, 'utf8'));
const lightweightCatalogue = {
  ...catalogue,
  programmes: catalogue.programmes.map((programme) => {
    const courseCount = (programme.courseGroups || []).reduce(
      (sum, group) => sum + (Array.isArray(group.courses) ? group.courses.length : 0),
      0
    );
    const { courseGroups, ...indexEntry } = programme;
    return {
      ...indexEntry,
      courseCount
    };
  })
};
const output = `// Generated by scripts/generate-tpg-catalog.js. Do not edit manually.\n`
  + `module.exports = ${JSON.stringify(lightweightCatalogue, null, 2)};\n`;

const checkOnly = process.argv.includes('--check');
if (checkOnly) {
  const current = fs.existsSync(targetPath) ? fs.readFileSync(targetPath, 'utf8') : '';
  if (current !== output) {
    console.error('Generated TPG catalogue is stale. Run npm run sync:tpg.');
    process.exit(1);
  }
} else {
  fs.writeFileSync(targetPath, output);
}
console.log(JSON.stringify({
  ok: true,
  checkOnly,
  universities: catalogue.universities.length,
  programmes: catalogue.programmes.length,
  target: path.relative(ROOT, targetPath)
}));
