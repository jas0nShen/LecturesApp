const fs = require('node:fs');
const path = require('node:path');

const ROOT = path.join(__dirname, '..');
const sourcePath = path.join(ROOT, 'data', 'tpg-programmes.json');
const targetPath = path.join(ROOT, 'miniprogram', 'utils', 'tpgCatalog.js');

const catalogue = JSON.parse(fs.readFileSync(sourcePath, 'utf8'));
const output = `// Generated by scripts/generate-tpg-catalog.js. Do not edit manually.\n`
  + `module.exports = ${JSON.stringify(catalogue, null, 2)};\n`;

fs.writeFileSync(targetPath, output);
console.log(JSON.stringify({
  ok: true,
  universities: catalogue.universities.length,
  programmes: catalogue.programmes.length,
  target: path.relative(ROOT, targetPath)
}));
