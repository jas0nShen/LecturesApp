const fs = require('node:fs');
const path = require('node:path');

const ROOT = path.join(__dirname, '..');
const sourcePath = path.join(ROOT, 'data', 'tpg-programmes.json');
const miniProgramRoot = path.join(ROOT, 'miniprogram');
const appConfigPath = path.join(miniProgramRoot, 'app.json');
const registryPath = path.join(miniProgramRoot, 'utils', 'tpgCourseShards.js');
const PACKAGE_PREFIX = 'tpg-data-';
const TARGET_BYTES = 1400 * 1024;

function slug(value) {
  return String(value || '').trim().toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
}

function splitRows(rows, targetBytes = TARGET_BYTES) {
  const shards = [];
  let current = [];
  let currentBytes = 2;
  rows.forEach((row) => {
    const rowBytes = Buffer.byteLength(JSON.stringify(row)) + 2;
    if (current.length && currentBytes + rowBytes > targetBytes) {
      shards.push(current);
      current = [];
      currentBytes = 2;
    }
    current.push(row);
    currentBytes += rowBytes;
  });
  if (current.length) shards.push(current);
  return shards;
}

function removeGeneratedPackages() {
  const subpackagesRoot = path.join(miniProgramRoot, 'subpackages');
  if (!fs.existsSync(subpackagesRoot)) return;
  fs.readdirSync(subpackagesRoot).filter((name) => name.startsWith(PACKAGE_PREFIX)).forEach((name) => {
    fs.rmSync(path.join(subpackagesRoot, name), { recursive: true, force: true });
  });
}

function writeShard({ universityCode, packageName, shardName, rows }) {
  const packageRoot = path.join(miniProgramRoot, 'subpackages', packageName);
  const dataDir = path.join(packageRoot, 'tpgCourseData');
  const loaderDir = path.join(packageRoot, 'pages', 'loader');
  fs.mkdirSync(dataDir, { recursive: true });
  fs.mkdirSync(loaderDir, { recursive: true });
  const files = buildShardFiles({ universityCode, packageName, shardName, rows });
  Object.entries(files).forEach(([relativePath, content]) => {
    fs.writeFileSync(path.join(packageRoot, relativePath), content);
  });
}

function buildShardFiles({ universityCode, packageName, shardName, rows }) {
  return {
    [`tpgCourseData/${shardName}.js`]: `// Generated by npm run sync:tpg. Do not edit manually.\nmodule.exports = ${JSON.stringify(rows, null, 2)};\n`,
    'pages/loader/index.json': '{}\n',
    'pages/loader/index.wxml': '<view class="loader">正在载入硕士课程数据…</view>\n',
    'pages/loader/index.wxss': '.loader { padding: 48rpx; color: #53605a; text-align: center; }\n',
    'pages/loader/index.js': `// Generated by npm run sync:tpg. Do not edit manually.\nconst programmes = require('../../tpgCourseData/${shardName}');\nconst universityCode = ${JSON.stringify(universityCode)};\nconst packageName = ${JSON.stringify(`subpackages/${packageName}`)};\n\nPage({\n  onLoad() {\n    const app = getApp();\n    if (typeof app.registerTpgProgrammeShard === 'function') {\n      app.registerTpgProgrammeShard({ universityCode, packageName, programmes });\n    }\n    const completeActivation = () => {\n      if (typeof app.completeTpgProgrammeShardActivation === 'function') {\n        app.completeTpgProgrammeShardActivation(packageName);\n      }\n    };\n    const returnToCaller = (attempt = 0) => wx.navigateBack({\n      delta: 1,\n      success: completeActivation,\n      fail() {\n        if (attempt < 4) setTimeout(() => returnToCaller(attempt + 1), 100);\n      }\n    });\n    setTimeout(() => returnToCaller(), 100);\n  }\n});\n`
  };
}

function buildRegistry(packageMap, counts) {
  const loaderEntries = Object.entries(packageMap).map(([code, packages]) => {
    const requires = packages.map((item) => `getNodeRequire()(\"../subpackages/${item.name}/tpgCourseData/${item.shardName}\")`).join(', ');
    return `  ${JSON.stringify(code)}: () => [].concat(${requires})`;
  }).join(',\n');
  const packageNames = Object.fromEntries(Object.entries(packageMap).map(([code, packages]) => [code, packages.map((item) => `subpackages/${item.name}`)]));
  return `// Generated by npm run sync:tpg. Do not edit manually.\nconst nodeLoaders = {\n${loaderEntries}\n};\nconst counts = ${JSON.stringify(counts, null, 2)};\nconst packageNames = ${JSON.stringify(packageNames, null, 2)};\nconst cache = {};\n\nfunction getNodeRequire() { return eval('require'); }\n\nfunction getProgrammesByUniversityCode(universityCode) {\n  const key = String(universityCode || '').toUpperCase();\n  if (typeof getApp === 'function') {\n    const app = getApp();\n    if (app && app.globalData && app.globalData.tpgProgrammesByUniversity) {\n      return app.globalData.tpgProgrammesByUniversity[key] || [];\n    }\n  }\n  if (!nodeLoaders[key]) return [];\n  if (!cache[key]) cache[key] = nodeLoaders[key]();\n  return cache[key];\n}\n\nfunction getProgrammeCount(universityCode) {\n  if (universityCode) return counts[String(universityCode || '').toUpperCase()] || 0;\n  return Object.values(counts).reduce((sum, count) => sum + count, 0);\n}\n\nfunction getPackageNames(universityCode) {\n  return packageNames[String(universityCode || '').toUpperCase()] || [];\n}\n\nmodule.exports = { getPackageNames, getProgrammeCount, getProgrammesByUniversityCode };\n`;
}

function main() {
  const catalogue = JSON.parse(fs.readFileSync(sourcePath, 'utf8'));
  const checkOnly = process.argv.includes('--check');
  if (!checkOnly) removeGeneratedPackages();
  const packageMap = {};
  const counts = {};
  catalogue.universities.forEach((university) => {
    const rows = catalogue.programmes.filter((programme) => programme.universityCode === university.code && Array.isArray(programme.courseGroups) && programme.courseGroups.length).map((programme) => ({
      id: programme.id,
      courseGroups: programme.courseGroups
    }));
    counts[university.code] = rows.length;
    if (!rows.length) return;
    const shards = splitRows(rows);
    packageMap[university.code] = shards.map((shardRows, index) => {
      const suffix = shards.length > 1 ? `-${index + 1}` : '';
      const name = `${PACKAGE_PREFIX}${slug(university.code)}${suffix}`;
      const shardName = `${slug(university.code)}${suffix}`;
      if (!checkOnly) writeShard({ universityCode: university.code, packageName: name, shardName, rows: shardRows });
      return { name, shardName, universityCode: university.code, rows: shardRows };
    });
  });
  const registry = buildRegistry(packageMap, counts);

  const appConfig = JSON.parse(fs.readFileSync(appConfigPath, 'utf8'));
  const retained = (appConfig.subpackages || []).filter((item) => !String(item.name || '').startsWith(PACKAGE_PREFIX));
  const generated = Object.values(packageMap).flat().map((item) => ({
    root: `subpackages/${item.name}`,
    name: item.name,
    pages: ['pages/loader/index']
  }));
  appConfig.subpackages = retained.concat(generated);
  if (checkOnly) {
    const mismatches = [];
    if (!fs.existsSync(registryPath) || fs.readFileSync(registryPath, 'utf8') !== registry) mismatches.push(path.relative(ROOT, registryPath));
    Object.values(packageMap).flat().forEach((item) => {
      const packageRoot = path.join(miniProgramRoot, 'subpackages', item.name);
      const files = buildShardFiles({ universityCode: item.universityCode, packageName: item.name, shardName: item.shardName, rows: item.rows });
      Object.entries(files).forEach(([relativePath, content]) => {
        const filePath = path.join(packageRoot, relativePath);
        if (!fs.existsSync(filePath) || fs.readFileSync(filePath, 'utf8') !== content) mismatches.push(path.relative(ROOT, filePath));
      });
    });
    const expectedNames = new Set(generated.map((item) => item.name));
    const subpackagesRoot = path.join(miniProgramRoot, 'subpackages');
    fs.readdirSync(subpackagesRoot).filter((name) => name.startsWith(PACKAGE_PREFIX) && !expectedNames.has(name)).forEach((name) => mismatches.push(path.join('miniprogram/subpackages', name)));
    const currentAppConfig = fs.readFileSync(appConfigPath, 'utf8');
    if (currentAppConfig !== `${JSON.stringify(appConfig, null, 2)}\n`) mismatches.push(path.relative(ROOT, appConfigPath));
    if (mismatches.length) {
      console.error(`Generated TPG course shards are stale:\n${mismatches.join('\n')}\nRun npm run sync:tpg.`);
      process.exit(1);
    }
  } else {
    fs.writeFileSync(registryPath, registry);
    fs.writeFileSync(appConfigPath, `${JSON.stringify(appConfig, null, 2)}\n`);
  }
  console.log(JSON.stringify({ ok: true, checkOnly, packages: generated.length, programmes: Object.values(counts).reduce((sum, count) => sum + count, 0) }));
}

if (require.main === module) main();

module.exports = { buildShardFiles, splitRows };
